function renderChapter5(data, userBrand) {
  const d = data;
  const itemsZh = Array.isArray(d.brandTakeaway) ? d.brandTakeaway : [];
  const itemsEn = Array.isArray(d.brandTakeawayEn) ? d.brandTakeawayEn : (d.i18n?.en?.brandTakeawayEn || []);
  // 只要任一语言有内容就显示；优先用较长的那一版作为主显示
  const items = itemsZh.length >= itemsEn.length ? itemsZh : itemsEn;
  const hasTakeaway = items.length > 0;
  const otherItems = itemsZh.length >= itemsEn.length ? itemsEn : itemsZh;
  const sameLen = otherItems.length === items.length;

  const ubZh = (userBrand || '').trim() || d.nameZh || d.name || '该品牌';
  const ubEn = (userBrand || '').trim() || d.nameEn || d.name || '该品牌';
  const nameZh = d.nameZh || d.name || '';
  const nameEn = d.nameEn || d.i18n?.en?.nameEn || d.name || '';

  // 缺失时显示兜底提示文案（双语）
  const body = hasTakeaway
    ? `<ul>${items.map((t, i) => {
        const tMain = esc(t);
        const tOther = esc(sameLen ? otherItems[i] : t);
        return `<li><span data-bi="${tMain}::${tOther}">${tMain}</span></li>`;
      }).join('')}</ul>`
    : `<p class="takeaway-empty">${b(STRINGS.zh.ch4.empty, STRINGS.en.ch4.empty)}</p>`;

  return `
    <section class="chapter" id="ch5">
      <div class="chapter-header">
        <span class="chapter-num">${b(STRINGS.zh.ch4.num, STRINGS.en.ch4.num)}</span>
        <h2>${b(STRINGS.zh.ch4.title(ubZh), STRINGS.en.ch4.title(ubEn))}</h2>
      </div>
      <p class="chapter-desc">${b(STRINGS.zh.ch4.desc(nameZh), STRINGS.en.ch4.desc(nameEn))}</p>
      <div class="takeaway">
        <h3>${b(STRINGS.zh.ch4.h3, STRINGS.en.ch4.h3)}</h3>
        ${body}
      </div>
    </section>`;
}

