// ---------- 工具：转义 ----------
const esc = (s) => String(s ?? '').replace(/&/g, '&amp;').replace(/</g, '&lt;').replace(/>/g, '&gt;').replace(/"/g, '&quot;');

// ---------- 双语取数：优先 data.*En（新结构），回退 data.i18n.en.*En，最后 data.*Zh（兼容旧） ----------
function pickEn(data, key) {
  return data[key + 'En'] || data.i18n?.en?.[key + 'En'] || data.i18n?.en?.[key] || (data[key] && /[一-龥]/.test(data[key]) ? '' : data[key]) || '';
}
function pickZh(data, key) {
  return data[key + 'Zh'] || data[key] || '';
}
function pickLang(data, key, lang) {
  return lang === 'en' ? pickEn(data, key) : pickZh(data, key);
}

// Bilingual span — server renders zh, JS swaps text on toggle
function b(zh, en) {
  const e = (en && en.trim()) ? en : (zh || '');
  return `<span data-bi="${esc(zh)}::${esc(e)}">${esc(zh || '')}</span>`;
}
function bArr(zhArr, enArr, sep) {
  const z = (zhArr || []).join(sep || '、');
  const e = (enArr && enArr.length === (zhArr || []).length ? enArr : (zhArr || [])).join(sep === '、' ? ', ' : (sep || ''));
  return b(z, e);
}
function bTpl(templateZh, templateEn, vars) {
  // 简单 {key} 插值 + 双语
  const fill = (t) => String(t || '').replace(/\{(\w+)\}/g, (_, k) => (vars && k in vars) ? vars[k] : ('{' + k + '}'));
  return b(fill(templateZh), fill(templateEn));
}

